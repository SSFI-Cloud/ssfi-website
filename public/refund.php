<h3 class="text-center">Refund and Cancellation Policy</h3>

At SSFI Bharat Skate, we aim to provide a smooth and transparent registration experience for all participants. This Refund and Cancellation Policy outlines the terms under which refunds or cancellations may be processed for event registrations made through our website https://www.ssfibharatskate.com.

1. Registration Fees
All registration fees paid through our website are considered non-refundable under normal circumstances. We advise participants to carefully review event details before completing the payment.

2. Cancellation by Participant
If a participant wishes to cancel their registration, no refund will be provided.

Registrations are non-transferable to another participant or event.

In exceptional situations (e.g., medical emergencies), participants may request a refund by emailing us at info@ssfibharatskate.com. However, the approval of such requests is at the sole discretion of the organizing committee.

3. Event Cancellation or Rescheduling
In the unlikely event that an event is cancelled by the organizers, a full or partial refund may be issued depending on the circumstances.

If the event is rescheduled, participants will be notified, and their registration will be automatically transferred to the new date. No refund will be provided unless the participant is unable to attend on the new date and requests a cancellation within a specified time frame.

4. Refund Processing
Approved refunds (if any) will be processed within 7 to 10 working days.

Refunds will be credited to the original payment method used during registration.

We are not responsible for any delay caused by banks or payment gateways.

5. Dispute Resolution
If you believe a refund has not been processed correctly or have concerns related to your transaction, please contact us at info@ssfibharatskate.com with your registration ID and payment details.

